/**
 * Returns today's quote based on the day of the year (0-indexed).
 * Stable within a day — changes at midnight.
 */
export const getDailyQuote = () => {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now - start;
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay) - 1; // 0-indexed (Jan 1 = 0)
  const safeIndex = ((dayOfYear % DAILY_QUOTES.length) + DAILY_QUOTES.length) % DAILY_QUOTES.length;
  return DAILY_QUOTES[safeIndex];
};
